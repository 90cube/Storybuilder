"""플롯 템플릿 정의. 데이터(구조 가이드)만 — 계산·부작용 없음."""

# key → (표시명, 단계 리스트). 단계는 LLM에 줄 구조 지침.
PLOTS: dict[str, tuple[str, list[str]]] = {
    "tri": ("긍정·중립·부정", [
        "중립: 신캐가 사건의 가장자리에 무심히 등장",
        "긍정: 신캐의 개입이 흐름에 보탬이 되는 듯 보임",
        "부정: 그 개입이 예상치 못한 대가·그늘을 드리움",
    ]),
    "kishōtenketsu": ("기승전결", [
        "기: 신캐의 처지와 갭 상황 제시",
        "승: 신캐가 사건의 인물·이해관계와 얽힘",
        "전: 국면을 뒤집는 신캐발(發) 전환",
        "결: 후행 앵커로 자연히 수렴",
    ]),
    "hero12": ("영웅의 12서사", [
        "일상→소명→거부→조력자→관문→시련→접근→시련의 핵→보상→귀환길→부활→귀환",
        "12단계를 갭 길이에 맞게 압축하되 부활·귀환이 후행 앵커와 맞물리게",
    ]),
    "five": ("발단·전개·위기·절정·결말", [
        "발단: 신캐 등장 배경",
        "전개: 갭 사건 속으로 진입",
        "위기: 세계관 제약과 충돌",
        "절정: 최대 긴장, 신캐의 선택",
        "결말: 후행 앵커 상태로 봉합",
    ]),
}


def guidance(plot_key: str) -> str:
    """플롯 키 → LLM 프롬프트에 넣을 구조 지침 문자열."""
    name, steps = PLOTS.get(plot_key, PLOTS["five"])
    lines = "\n".join(f"  - {s}" for s in steps)
    return f"플롯 구조 = 「{name}」\n{lines}"
